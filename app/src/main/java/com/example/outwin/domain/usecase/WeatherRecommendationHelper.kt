    private fun String.escapeHtml(): String {
        return this.replace("&", "&amp;")
            .replace("<", "&lt;")
            .replace(">", "&gt;")
            .replace("\"", "&quot;")
            .replace("'", "&#39;")
    }

    fun getDayWeatherCondition(todayForecasts: List<ForecastItem>): String {
        val hasPrecipitation = todayForecasts.any {
            val mainWeather = it.weather.firstOrNull()?.main ?: ""
            mainWeather.contains("Rain", ignoreCase = true) ||
                    mainWeather.contains("Snow", ignoreCase = true) ||
                    mainWeather.contains("Drizzle", ignoreCase = true)
        }
        if (hasPrecipitation) return "Осадки"

        val hasClouds = todayForecasts.any {
            it.weather.firstOrNull()?.main?.contains("Clouds", ignoreCase = true) == true
        }
        if (hasClouds) return "Облачно"

        return "Ясно"
    }

